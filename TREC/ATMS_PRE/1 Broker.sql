CREATE TABLE APP_TYPE (
    --APP_TYPE_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    APP_TYPE_ID NUMBER(10,0) PRIMARY KEY,
    APP_NAME VARCHAR2(255 CHAR),
    REMARKS VARCHAR2(255 CHAR)
);

CREATE TABLE APP_REQUIRED_DOC (
    --APP_REQUIRED_DOC_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    APP_REQUIRED_DOC_ID NUMBER(10,0) PRIMARY KEY,
    APP_TYPE_ID NUMBER(10,0) NOT NULL,
    FILE_NAME VARCHAR2(255 CHAR) NOT NULL,
    STATUS VARCHAR2(50 CHAR) NOT NULL CHECK (STATUS IN ('ACTIVE','INACTIVE')),
    REMARKS VARCHAR2(255 CHAR),
    ADDED_AT      DATE DEFAULT SYSDATE NOT NULL,        
    ADDED_BY      VARCHAR2(50 CHAR),                  
    UPDATED_AT    DATE,                                
    UPDATED_BY    VARCHAR2(50 CHAR),
    CONSTRAINT FK_APP_REQUIRED_DOC_APP_TYPE FOREIGN KEY (APP_TYPE_ID) REFERENCES APP_TYPE (APP_TYPE_ID)
);


CREATE TABLE BROKER_RENEWAL_APP_MASTER (
    --BROKER_RENEWAL_APP_MASTER_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BROKER_RENEWAL_APP_MASTER_ID NUMBER(10,0) PRIMARY KEY,
    APP_TYPE_ID NUMBER(10,0) NOT NULL,
    BROKER_ID NUMBER(5,0) NOT NULL,
    FIELD1 VARCHAR2(255 CHAR),
    FIELD2 VARCHAR2(255 CHAR),
    REMARKS VARCHAR2(255 CHAR),
    ADDED_AT      DATE DEFAULT SYSDATE NOT NULL,
    ADDED_BY      VARCHAR2(50 CHAR),
    UPDATED_AT    DATE,
    UPDATED_BY    VARCHAR2(50 CHAR),

    CONSTRAINT FK_BRAM_APP_TYPE FOREIGN KEY (APP_TYPE_ID) REFERENCES APP_TYPE (APP_TYPE_ID),
    CONSTRAINT FK_BRAM_BROKER FOREIGN KEY (BROKER_ID) REFERENCES BROKER (BROKER_ID)
);


CREATE TABLE BROKER_RENEWAL_APP_DETAILS (
    --BROKER_RENEWAL_APP_DETAILS_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BROKER_RENEWAL_APP_DETAILS_ID NUMBER(10,0) PRIMARY KEY,
    BROKER_RENEWAL_APP_MASTER_ID  NUMBER(10,0) NOT NULL,
    APP_REQUIRED_DOC_ID           NUMBER(10,0) NOT NULL,
    FILE_PATH VARCHAR2(255 CHAR),
    STATUS VARCHAR2(50 CHAR) NOT NULL CHECK (STATUS IN ('VALID','INVALID')),
    REMARKS VARCHAR2(255 CHAR),
    ADDED_AT DATE DEFAULT SYSDATE NOT NULL,
    ADDED_BY VARCHAR2(50 CHAR),
    UPDATED_AT DATE,
    UPDATED_BY VARCHAR2(50 CHAR),

    CONSTRAINT FK_BRAD_MASTER FOREIGN KEY (BROKER_RENEWAL_APP_MASTER_ID) REFERENCES BROKER_RENEWAL_APP_MASTER (BROKER_RENEWAL_APP_MASTER_ID),
    CONSTRAINT FK_BRAD_DOC FOREIGN KEY (APP_REQUIRED_DOC_ID) REFERENCES APP_REQUIRED_DOC (APP_REQUIRED_DOC_ID)
);

CREATE TABLE BROKER_RENEWAL_APP_APPROVAL (
    --BROKER_RENEWAL_APP_APPROVAL_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BROKER_RENEWAL_APP_APPROVAL_ID NUMBER(10,0) PRIMARY KEY,
    BROKER_RENEWAL_APP_MASTER_ID   NUMBER(10,0) NOT NULL,
    STATUS VARCHAR2(50 CHAR) NOT NULL CHECK (STATUS IN ('DRAFT','CREATED','VERIFIED','RESENT','APPROVED','REJECTED')),
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL CHECK (IS_ACTIVE IN (0,1)),
    ADDED_AT DATE DEFAULT SYSDATE NOT NULL,
    ADDED_BY VARCHAR2(50 CHAR),
    UPDATED_AT DATE,
    UPDATED_BY VARCHAR2(50 CHAR),

    CONSTRAINT FK_BRAPP_MASTER FOREIGN KEY (BROKER_RENEWAL_APP_MASTER_ID) REFERENCES BROKER_RENEWAL_APP_MASTER (BROKER_RENEWAL_APP_MASTER_ID)
);

