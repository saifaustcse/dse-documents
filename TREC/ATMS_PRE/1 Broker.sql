TREC_DSESMS.VW_BROKER_RENEWAL_APP
------------------------------------------------------

CREATE OR REPLACE FORCE VIEW "TREC_TREC"."VW_APP_REQUIRED_DOC" 
("APP_REQUIRED_DOC_ID", "FILE_NAME", "STATUS", "REMARKS", "ADDED_AT", "ADDED_BY", "UPDATED_AT", "UPDATED_BY",
"APP_TYPE_ID", "APP_NAME"
) AS 
SELECT 
    AD.APP_REQUIRED_DOC_ID, 
    AD.FILE_NAME,
    AD.STATUS,
    AD.REMARKS, 
    AD.ADDED_AT, 
    AD.ADDED_BY, 
    AD.UPDATED_AT,
    AD.UPDATED_BY,
    AT.APP_TYPE_ID,
    AT.APP_NAME
FROM APP_REQUIRED_DOC AD 
JOIN APP_TYPE AT ON AT.APP_TYPE_ID = AD.APP_TYPE_ID
WHERE AD.STATUS = 'ACTIVE';

------------------------------------------------------
TREC_DSESMS.VW_BROKER_RENEWAL_APP
------------------------------------------------------

CREATE OR REPLACE FORCE VIEW "TREC_TREC"."VW_BROKER_RENEWAL_APP" 
(
--"VW_ID", 
"BROKER_RENEWAL_APP_MASTER_ID", "APP_TYPE_ID", "BROKER_ID", "FIELD1", "FIELD2", "MASTER_REMARKS", "MASTER_ADDED_AT", "MASTER_ADDED_BY", "MASTER_UPDATED_AT", "MASTER_UPDATED_BY", 
"APP_REQUIRED_DOC_ID", "DOC_FILE_NAME",
"BROKER_RENEWAL_APP_DETAILS_ID","FILE_PATH", "DETAIL_STATUS", "DETAIL_REMARKS", "DETAIL_ADDED_AT", "DETAIL_ADDED_BY", "DETAIL_UPDATED_AT", "DETAIL_UPDATED_BY", 
"BROKER_RENEWAL_APP_APPROVAL_ID", "APPROVAL_STATUS", "APPROVAL_IS_ACTIVE", "APPROVAL_ADDED_AT", "APPROVAL_ADDED_BY", "APPROVAL_UPDATED_AT", "APPROVAL_UPDATED_BY") AS 

SELECT
    --ROWID AS VW_ID,  -- EDMX-friendly primary key
    M.BROKER_RENEWAL_APP_MASTER_ID,
    M.APP_TYPE_ID,
    M.BROKER_ID,
    M.FIELD1,
    M.FIELD2,
    M.REMARKS AS MASTER_REMARKS,
    M.ADDED_AT AS MASTER_ADDED_AT,
    M.ADDED_BY AS MASTER_ADDED_BY,
    M.UPDATED_AT AS MASTER_UPDATED_AT,
    M.UPDATED_BY AS MASTER_UPDATED_BY,
    
    R.APP_REQUIRED_DOC_ID,
    R.FILE_NAME AS DOC_FILE_NAME,
    
    D.BROKER_RENEWAL_APP_DETAILS_ID,
    D.FILE_PATH,
    D.STATUS AS DETAIL_STATUS,
    D.REMARKS AS DETAIL_REMARKS,
    D.ADDED_AT AS DETAIL_ADDED_AT,
    D.ADDED_BY AS DETAIL_ADDED_BY,
    D.UPDATED_AT AS DETAIL_UPDATED_AT,
    D.UPDATED_BY AS DETAIL_UPDATED_BY,
    
    A.BROKER_RENEWAL_APP_APPROVAL_ID,
    A.STATUS AS APPROVAL_STATUS,
    A.IS_ACTIVE AS APPROVAL_IS_ACTIVE,
    A.ADDED_AT AS APPROVAL_ADDED_AT,
    A.ADDED_BY AS APPROVAL_ADDED_BY,
    A.UPDATED_AT AS APPROVAL_UPDATED_AT,
    A.UPDATED_BY AS APPROVAL_UPDATED_BY
FROM BROKER_RENEWAL_APP_MASTER M
INNER JOIN BROKER_RENEWAL_APP_APPROVAL A ON M.BROKER_RENEWAL_APP_MASTER_ID = A.BROKER_RENEWAL_APP_MASTER_ID
INNER JOIN BROKER_RENEWAL_APP_DETAILS D ON M.BROKER_RENEWAL_APP_MASTER_ID = D.BROKER_RENEWAL_APP_MASTER_ID
INNER JOIN APP_REQUIRED_DOC R ON D.APP_REQUIRED_DOC_ID = R.APP_REQUIRED_DOC_ID;










