CREATE TABLE DEALER_RENEWAL_APP_MASTER (
    --DEALER_RENEWAL_APP_MASTER_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DEALER_RENEWAL_APP_MASTER_ID NUMBER(10,0) PRIMARY KEY,
    APP_TYPE_ID NUMBER(10,0) NOT NULL,
    DEALER_ID NUMBER(5,0) NOT NULL,
    FIELD1 VARCHAR2(255 CHAR),
    FIELD2 VARCHAR2(255 CHAR),
    REMARKS VARCHAR2(255 CHAR),
    ADDED_AT      DATE DEFAULT SYSDATE NOT NULL,
    ADDED_BY      VARCHAR2(50 CHAR),
    UPDATED_AT    DATE,
    UPDATED_BY    VARCHAR2(50 CHAR),

    CONSTRAINT FK_DRAM_APP_TYPE FOREIGN KEY (APP_TYPE_ID) REFERENCES APP_TYPE (APP_TYPE_ID),
    CONSTRAINT FK_DRAM_DEALER FOREIGN KEY (DEALER_ID) REFERENCES DEALER (DEALER_ID)
);


CREATE TABLE DEALER_RENEWAL_APP_DETAILS (
    --DEALER_RENEWAL_APP_DETAILS_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DEALER_RENEWAL_APP_DETAILS_ID NUMBER(10,0) PRIMARY KEY,
    DEALER_RENEWAL_APP_MASTER_ID  NUMBER(10,0) NOT NULL,
    APP_REQUIRED_DOC_ID           NUMBER(10,0) NOT NULL,
    FILE_PATH VARCHAR2(255 CHAR),
    STATUS VARCHAR2(50 CHAR) NOT NULL CHECK (STATUS IN ('VALID','INVALID')),
    REMARKS VARCHAR2(255 CHAR),
    ADDED_AT DATE DEFAULT SYSDATE NOT NULL,
    ADDED_BY VARCHAR2(50 CHAR),
    UPDATED_AT DATE,
    UPDATED_BY VARCHAR2(50 CHAR),

    CONSTRAINT FK_DRAD_MASTER FOREIGN KEY (DEALER_RENEWAL_APP_MASTER_ID) REFERENCES DEALER_RENEWAL_APP_MASTER (DEALER_RENEWAL_APP_MASTER_ID),
    CONSTRAINT FK_DRAD_DOC FOREIGN KEY (APP_REQUIRED_DOC_ID) REFERENCES APP_REQUIRED_DOC (APP_REQUIRED_DOC_ID)
);

CREATE TABLE DEALER_RENEWAL_APP_APPROVAL (
    --DEALER_RENEWAL_APP_APPROVAL_ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DEALER_RENEWAL_APP_APPROVAL_ID NUMBER(10,0) PRIMARY KEY,
    DEALER_RENEWAL_APP_MASTER_ID   NUMBER(10,0) NOT NULL,
    STATUS VARCHAR2(50 CHAR) NOT NULL CHECK (STATUS IN ('DRAFT','CREATED','VERIFIED','RESENT','APPROVED','REJECTED')),
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL CHECK (IS_ACTIVE IN (0,1)),
    ADDED_AT DATE DEFAULT SYSDATE NOT NULL,
    ADDED_BY VARCHAR2(50 CHAR),
    UPDATED_AT DATE,
    UPDATED_BY VARCHAR2(50 CHAR),

    CONSTRAINT FK_DRAPP_MASTER FOREIGN KEY (DEALER_RENEWAL_APP_MASTER_ID) REFERENCES DEALER_RENEWAL_APP_MASTER (DEALER_RENEWAL_APP_MASTER_ID)
);


