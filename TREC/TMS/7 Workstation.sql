CREATE OR REPLACE FORCE VIEW "TREC_TREC"."VW_TREC_SUMMARY_COUNT" ("TREC_ID", "TREC_NO", "TREC_CODE", "TREC_NAME", "STATUS", "IS_BROKER", "IS_DEALER", "OFFICE_COUNT", "EXTENSION_COUNT", "BRANCH_COUNT", "DIGITAL_BOOTH_COUNT", "WORKSTATION_COUNT", "AR_COUNT", "BROKER_DEALER_STATUS") AS 
select r.TREC_ID, r.TREC_NO, r.CODE,  r.NAME,  r.STATUS, r.IS_BROKER, r.IS_DEALER,  r.OFFICE_COUNT, r.EXTENSION_COUNT, r.BRANCH_COUNT, r.DIGITAL_BOOTH_COUNT, r.WORKSTATION_COUNT, r.AR_COUNT,
       CASE
        WHEN (r.IS_BROKER = 1 AND r.IS_DEALER = 1) THEN 'BROKER_DEALER'
        WHEN (r.IS_BROKER = 1 AND r.IS_DEALER = 0) THEN 'BROKER'
        WHEN (r.IS_BROKER = 0 AND r.IS_DEALER = 1) THEN 'DEALER'
        ELSE 'NONE'
      END AS BROKER_DEALER_STATUS
from (
SELECT
    t.TREC_ID,
    t.TREC_NO,
    t.CODE,
    t.NAME,
    t.STATUS,
    (
        SELECT COUNT(*) 
        FROM BROKER b
        INNER JOIN BROKER_STATUS bs on b.BROKER_ID = bs.BROKER_ID  AND bs.STATUS IN ('ACTIVE', 'SUSPENDED') AND bs.IS_ACTIVE = 1 -- 'ACTIVE', 'SUSPENDED', 'CANCELLED'
        WHERE b.TREC_ID = t.TREC_ID
    ) as IS_BROKER,
    (
        SELECT COUNT(*) 
        FROM DEALER d
        INNER JOIN DEALER_STATUS ds on d.DEALER_ID = ds.DEALER_ID  AND ds.STATUS IN ('ACTIVE', 'SUSPENDED') AND ds.IS_ACTIVE = 1 -- 'ACTIVE', 'SUSPENDED', 'CANCELLED'
        WHERE d.TREC_ID = t.TREC_ID
    ) as IS_DEALER,
    (
        SELECT COUNT(*) 
        FROM OFFICE o
        INNER JOIN OFFICE_TYPE ot on o.OFFICE_ID = ot.OFFICE_ID AND ot.IS_ACTIVE = 1
        INNER JOIN OFFICE_STATUS os on o.OFFICE_ID = os.OFFICE_ID AND os.STATUS IN ('ACTIVE', 'RESUMED') AND os.IS_ACTIVE = 1  --'ACTIVE', 'RESUMED', 'SUSPENDED', 'WITHDRAWN'
        WHERE o.TREC_ID = t.TREC_ID
    ) AS OFFICE_COUNT,
    (
        SELECT COUNT(*) 
        FROM OFFICE o
        INNER JOIN OFFICE_TYPE ot on o.OFFICE_ID = ot.OFFICE_ID AND ot.TYPE = 'EXTENSION' AND ot.IS_ACTIVE = 1
        INNER JOIN OFFICE_STATUS os on o.OFFICE_ID = os.OFFICE_ID  AND os.STATUS IN ('ACTIVE', 'RESUMED') AND os.IS_ACTIVE = 1 --'ACTIVE', 'RESUMED', 'SUSPENDED', 'WITHDRAWN'
        WHERE o.TREC_ID = t.TREC_ID
    ) AS EXTENSION_COUNT,
    (
        SELECT COUNT(*) 
        FROM OFFICE o
        INNER JOIN OFFICE_TYPE ot on o.OFFICE_ID = ot.OFFICE_ID  AND ot.TYPE = 'BRANCH' AND ot.IS_ACTIVE = 1
        INNER JOIN OFFICE_STATUS os on o.OFFICE_ID = os.OFFICE_ID  AND os.STATUS IN ('ACTIVE', 'RESUMED') AND  os.IS_ACTIVE = 1 --'ACTIVE', 'RESUMED', 'SUSPENDED', 'WITHDRAWN'
        WHERE o.TREC_ID = t.TREC_ID
    ) AS BRANCH_COUNT,
    (
        SELECT COUNT(*) 
        FROM OFFICE o
        INNER JOIN OFFICE_TYPE ot on o.OFFICE_ID = ot.OFFICE_ID AND ot.TYPE = 'DIGITAL BOOTH' AND ot.IS_ACTIVE = 1
        INNER JOIN OFFICE_STATUS os on o.OFFICE_ID = os.OFFICE_ID  AND os.STATUS IN ('ACTIVE', 'RESUMED') AND os.IS_ACTIVE = 1 --'ACTIVE', 'RESUMED', 'SUSPENDED', 'WITHDRAWN'
        WHERE o.TREC_ID = t.TREC_ID
    ) AS DIGITAL_BOOTH_COUNT,
    (
        SELECT COUNT(*) 
        FROM WORKSTATION w
        INNER JOIN WORKSTATION_STATUS ws on w.WORKSTATION_ID = ws.WORKSTATION_ID AND ws.STATUS IN ('ACTIVE') AND ws.IS_ACTIVE = 1 --'ACTIVE', 'SELF_SUSPENDED', 'SUSPENDED_BY_DSE', 'DEACTIVATED_BY_DSE'
        WHERE w.TREC_ID = t.TREC_ID
    ) AS WORKSTATION_COUNT,
    (
        SELECT COUNT(*) 
        FROM AUTH_REP ar
        inner join AUTH_REP_STATUS st on st.AUTH_REP_ID = ar.AUTH_REP_ID and st.STATUS IN ('ACTIVE', 'RESUMED') AND st.IS_ACTIVE = 1 --'ACTIVE','SUSPENDED','RESUMED', 'INACTIVE','WITHDRAWN'
        WHERE ar.TREC_ID = t.TREC_ID
    ) AS AR_COUNT
FROM
    TREC t
ORDER BY t.TREC_ID
) r;

select * from VW_TREC_SUMMARY_COUNT;

