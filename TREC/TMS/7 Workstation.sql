CREATE OR REPLACE FORCE VIEW "TREC_TREC"."VW_WORKSTATION" 
("TREC_ID", "TREC_NO", "TREC_CODE", "TREC_NAME", 
 "WORKSTATION_ID", "TWS_NAME", "TWS_CATERGORY", "TDRNO", "UCODE", "ID_TYPE", "EXP_DATE", "REMARKS", "ADDED_AT", "ADDED_BY",
 "WORKSTATION_STATUS_ID", "STATUS","STATUS_EFFECTIVE_DATE", "STATUS_END_DATE", "IS_ACTIVE",
 "OFFICE_ID", "OFFICE_NO",  "OFFICE_NAME", 
 "OFFICE_TYPE_ID", "TYPE",
 "LOCATION", "AREA", "THANA", "EUNION", "DISTRICT", "DIVISION", "IS_PANEL_BROKING", "PANEL_BROKER", "SPACE_SIZE", "IS_RENTED", "RENTED_FROM_DATE", "RENTED_TO_DATE",
 "PERSONNEL_ID","PERSONNEL_NAME","DESIGNATION","SEX","NID","OFFICIAL_CONTACT_NO", "PERSONAL_CONTACT_NO", "OFFICIAL_EMAIL", "PERSONAL_EMAIL",
  "AUTH_REP_ID", "BSEC_REG_NO"
 ) AS 
 
SELECT
    t.TREC_ID,
    t.TREC_NO,
    t.CODE as TREC_CODE,
    t.NAME as TREC_NAME,

    w.WORKSTATION_ID,
    w.TWS_NAME,
    w.TWS_CATERGORY,
    w.TDRNO,
    w.UCODE,
    w.ID_TYPE,
    w.EXP_DATE,
    w.REMARKS,
    w.ADDED_AT,
    w.ADDED_BY,

    st.WORKSTATION_STATUS_ID,
    st.STATUS,
    st.EFFECTIVE_DATE as STATUS_EFFECTIVE_DATE,
    st.END_DATE as STATUS_END_DATE,
    CASE WHEN st.STATUS IN ('ACTIVE') THEN 1 ELSE 0 END AS IS_ACTIVE,
    
    o.OFFICE_ID,
    o.OFFICE_NO,
    o.OFFICE_NAME,

    o.OFFICE_TYPE_ID,
    o.TYPE,
  
    o.LOCATION,
    o.AREA,
    o.THANA,
    o.EUNION,
    o.DISTRICT,
    o.DIVISION,
    o.IS_PANEL_BROKING,
    o.PANEL_BROKER,
    o.SPACE_SIZE,
    o.IS_RENTED,
    o.RENTED_FROM_DATE,
    o.RENTED_TO_DATE,

    pi.PERSONNEL_ID,
    pi.NAME as PERSONNEL_NAME,
    pi.DESIGNATION,
    pi.SEX,
    pi.NID,
    pi.OFFICIAL_CONTACT_NO,
    pi.PERSONAL_CONTACT_NO,
    pi.OFFICIAL_EMAIL,
    pi.PERSONAL_EMAIL,

    ar.AUTH_REP_ID,
    ar.BSEC_REG_NO
    
FROM  WORKSTATION w
INNER JOIN TREC t ON w.TREC_ID = t.TREC_ID
INNER JOIN WORKSTATION_STATUS st ON st.WORKSTATION_ID = w.WORKSTATION_ID AND st.IS_ACTIVE = 1
LEFT JOIN WORKSTATION_OFFICE wo ON w.WORKSTATION_ID = wo.WORKSTATION_ID AND wo.IS_ACTIVE = 1
LEFT JOIN VW_OFFICE o ON wo.OFFICE_ID = o.OFFICE_ID
LEFT JOIN WORKSTATION_AUTH_REP war ON w.WORKSTATION_ID = war.WORKSTATION_ID AND war.IS_ACTIVE = 1
LEFT JOIN PERSONNEL pi ON pi.PERSONNEL_ID = war.PERSONNEL_ID
LEFT JOIN AUTH_REP ar ON pi.PERSONNEL_ID = ar.PERSONNEL_ID
ORDER BY w.WORKSTATION_ID;

