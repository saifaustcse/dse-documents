	TWS_NAME VARCHAR2(10),
	TWS_CATERGORY VARCHAR2(4),
	TDRNO NUMBER(10),
    UCODE VARCHAR2(10),
	ID_TYPE VARCHAR2(1),
	EXP_DATE DATE,

    REMARKS VARCHAR2(255 CHAR), 
