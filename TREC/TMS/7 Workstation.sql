    ADDED_BY VARCHAR2(50) NOT NULL,

    UPDATED_AT DATE,
    UPDATED_BY VARCHAR2(50),

    constraint PK_WORKSTATION primary key (WORKSTATION_ID),
    constraint FK_WORKSTATION_TREC foreign key (TREC_ID) references TREC(TREC_ID)
