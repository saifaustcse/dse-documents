SELECT
    t.TREC_ID,
    t.TREC_NO,
    t.CODE as TREC_CODE,
    t.NAME as TREC_NAME,

    o.OFFICE_ID,
    o.OFFICE_NO,
    o.OFFICE_NAME,
    o.OFFICE_CONTACT_NO,
    o.OFFICE_EMAIL,
    o.BSEC_APPROVAL_DATE,
    o.DSE_APPROVAL_DATE,
    o.REMARKS,

    ot.OFFICE_TYPE_ID,
    ot.TYPE,
    ot.EFFECTIVE_DATE as TYPE_EFFECTIVE_DATE, 
    ot.END_DATE as TYPE_END_DATE, 
    
    oa.OFFICE_ADDRESS_ID,
    oa.LOCATION,
    oa.AREA,
    oa.THANA,
    oa.EUNION,
    oa.DIVISION,
    oa.DISTRICT,
    --div.DIVISION_ID,
    --div.DIVISION_NAME,
    --dis.DISTRICT_ID,
    --dis.DISTRICT_NAME,
    oa.OPERATION_START_DATE,
    oa.OPERATION_END_DATE,
    oa.IS_PANEL_BROKING,
    oa.PANEL_BROKER,
    oa.SPACE_SIZE,
    oa.IS_RENTED,
    oa.RENTED_FROM_DATE,
    oa.RENTED_TO_DATE,

    st.OFFICE_STATUS_ID,
    st.STATUS,
    st.EFFECTIVE_DATE as STATUS_EFFECTIVE_DATE,
    st.END_DATE as STATUS_END_DATE,
    CASE WHEN st.STATUS IN ('ACTIVE', 'RESUMED') THEN 1 ELSE 0 END AS IS_ACTIVE,

    pioi.PERSONNEL_ID As INCHARGE_PERSONNEL_ID,
    pioi.NAME as INCHARGE_NAME,
    pioi.DESIGNATION as INCHARGE_DESIGNATION,
    pioi.SEX as INCHARGE_SEX,
    pioi.NID as INCHARGE_NID,
    pioi.OFFICIAL_CONTACT_NO as INCHARGE_OFFICIAL_CONTACT_NO,
    pioi.PERSONAL_CONTACT_NO as INCHARGE_PERSONAL_CONTACT_NO,
    pioi.OFFICIAL_EMAIL as INCHARGE_OFFICIAL_EMAIL,
    pioi.PERSONAL_EMAIL as INCHARGE_PERSONAL_EMAIL,
    
    pioco.PERSONNEL_ID As CO_PERSONNEL_ID,
    pioco.NAME as CO_NAME,
    pioco.DESIGNATION as CO_DESIGNATION,
    pioco.SEX as CO_SEX,
    pioco.NID as CO_NID,
    pioco.OFFICIAL_CONTACT_NO as CO_OFFICIAL_CONTACT_NO,
    pioco.PERSONAL_CONTACT_NO as CO_PERSONAL_CONTACT_NO,
    pioco.OFFICIAL_EMAIL as CO_OFFICIAL_EMAIL,
    pioco.PERSONAL_EMAIL as CO_PERSONAL_EMAIL

FROM OFFICE o
INNER JOIN TREC t ON o.TREC_ID = t.TREC_ID
INNER JOIN OFFICE_TYPE ot on o.OFFICE_ID = ot.OFFICE_ID AND ot.IS_ACTIVE = 1
INNER JOIN OFFICE_STATUS st ON o.OFFICE_ID = st.OFFICE_ID AND st.IS_ACTIVE = 1
LEFT JOIN OFFICE_ADDRESS oa ON o.OFFICE_ID = oa.OFFICE_ID AND oa.IS_ACTIVE = 1
LEFT JOIN OFFICE_INCHARGE oi ON o.OFFICE_ID = oi.OFFICE_ID AND oi.IS_ACTIVE = 1
LEFT JOIN PERSONNEL pioi ON pioi.PERSONNEL_ID = oi.PERSONNEL_ID
LEFT JOIN OFFICE_COMPL_OFFICER oco ON o.OFFICE_ID = oco.OFFICE_ID AND oco.IS_ACTIVE = 1
LEFT JOIN PERSONNEL pioco ON pioco.PERSONNEL_ID = oco.PERSONNEL_ID
ORDER BY o.OFFICE_ID;

