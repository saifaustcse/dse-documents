	DSE_APPROVAL_DATE DATE, 

    OFFICE_CONTACT_NO VARCHAR2(50 CHAR),   -- OFFICE_BACKUP.CONTACTNO
    OFFICE_EMAIL VARCHAR2(255 CHAR),

