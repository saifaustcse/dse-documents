CREATE TABLE OFFICE(
    OFFICE_ID NUMBER(5,0)NOT NULL,
    BACKUP_ID NUMBER(5,0),
	TREC_ID NUMBER(5,0) NOT NULL,         -- OFFICE_BACKUP.MEM_NO  
	-- OFFICE_TYPE VARCHAR2(40 CHAR),     -- OFFICE_BACKUP.BRNCHTYPE 
